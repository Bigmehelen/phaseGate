def first(request = False):
	if request == True:
		count = 0
		check = 0
		while count < 2:

			print("""
		What is the Capital of Lagos
			options:
			A. Ikeja
			B. Abia
			C. London
			""")
			result = str(input("Choose your answer: "))
			result = result.upper()

			if result == 'A':
				print("Correct")
				count += 1
				continue

			elif result == 'B':
				print("Incorrect")
				check += 1
				continue

			elif result == 'C':
				print("Incorrect")
				check += 1
				continue

			else:
				print("Invalid input")

		return False

